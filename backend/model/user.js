const mongoose = require("mongoose");
const userSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  pic: {
    type: String,
    default:
      "https://res.cloudinary.com/dt0isai38/image/upload/v1674850149/default_kmiptn.png",
  },
});

module.exports = mongoose.model("User", userSchema);
